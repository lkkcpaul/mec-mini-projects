### Spider that crawls and goes to the next page automatically
import scrapy

#### Scraper that can scrape all quotes of particular paths
### Using XPath
class XPathSpider(scrapy.Spider):
    name = "toscrape-xpath"

    def start_requests(self):
        url = 'http://quotes.toscrape.com/'
        tag = getattr(self, 'tag', None)
        if tag is not None:
            url = url + 'tag/' + tag
        yield scrapy.Request(url, self.parse)

    def parse(self, response):
        for quote in response.xpath('//div[@class = "quote"]'):####
            yield {
                'text': quote.xpath('.//span[@class = "text"]/text()').get(),
                'author': quote.xpath('.//small[@class = "author"]/text()').get(),
            }

        next_page = response.xpath('//li[@class = "next"]/a/@href').get()
        if next_page is not None:
            yield response.follow(next_page, self.parse)

### Scraper that scrapes all the author infos #################
### Using CSS  ############################################
class CSSSpider(scrapy.Spider):
    name = 'toscrape-css'

    start_urls = ['http://quotes.toscrape.com/']

    def parse(self, response):
        author_page_links = response.css('.author + a')
        yield from response.follow_all(author_page_links, self.parse_author)

        pagination_links = response.css('li.next a')
        yield from response.follow_all(pagination_links, self.parse)

    def parse_author(self, response):
        def extract_with_css(query):
            return response.css(query).get(default='').strip()

        yield {
            'name': extract_with_css('h3.author-title::text'),
            'birthdate': extract_with_css('.author-born-date::text'),
            'bio': extract_with_css('.author-description::text'),
        }



